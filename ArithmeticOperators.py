x = 20
y = 3

#Output: x + y = 23
print('x + y =',x+y)

#Output: x- y = 17
print('x - y =',x-y)

#Output: x * y = 60
print('x * y =',x*y)

#Output: x / y = 6.666666666666667
print('x / y =',x/y)

#Output: x % y = 2 หารแล้วเอาเศษมาแสดง
print('x % y =',x%y)

#Output: x // y = 6 เป็นการหารแบบปัดเศษลง
print('x // y =',x//y)

#Output: x ** y = 8000 ยกกำลัง
print('x ** y =',x**y)